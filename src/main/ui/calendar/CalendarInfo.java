package main.ui.calendar;

public class CalendarInfo {
	
	private int month;
	private int year;
	
	public CalendarInfo(int month, int year) {
		this.month = month;
		this.year = year;
	}
	
	public int getMonth() {
		return month;
	}
	
	public int getYear() {
		return year;
	}

}
